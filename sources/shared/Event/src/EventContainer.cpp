#include "EventContainer.hpp"
#include "Event.hpp"

//////////////////////////////////////////////////////////////////////
/////	Ctor/Dtor
//////////////////////////////////////////////////////////////////////

EventContainer::EventContainer(void)
{
  _events.clear();
}

EventContainer::~EventContainer(void)
{
}

//////////////////////////////////////////////////////////////////////
/////	Utility functions
//////////////////////////////////////////////////////////////////////

void	EventContainer::clearEvents(void)
{
  _events.clear();
}

void	EventContainer::addEvent(eventType type, s_event event)
{
  _events.push_back(std::pair<eventType, s_event>(type, event));
}

//////////////////////////////////////////////////////////////////////
/////	Getter
//////////////////////////////////////////////////////////////////////

std::list<std::pair<eventType, s_event> >* EventContainer::getEvents(void)
{
  return &_events;
}
